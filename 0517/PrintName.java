// 자기 이름 출력하는 프로그램
public class PrintName {
    public static void main(String[] args) {
        System.out.println("김효정1");
        System.out.println("김효정2");
        System.out.println("김효정3");
        System.out.println("김효정4");
        System.out.println("김효정5");

        System.out.println("10" + "10"); // 문자열의 형태로 인식 -> 1010
        System.out.println("11" + "11");
        System.out.println("12" + "12");

        System.out.println(20 + 20); // 정수의 형태로 인식 -> 40
        System.out.println(21 + 21);
        System.out.println(22 + 22);
    }
}