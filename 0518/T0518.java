// 두 수 44와 72의 + - * 각각의 연산결과를 출력해보세요.

public class T0518 {
    public static void main(String args[]){
		//리터널 상수: 고정된 값
        System.out.println(44 + 72);
        System.out.println(44 - 72);
        System.out.println(44 * 72);
		System.out.println(44 / 72);
		
		int x = 44;
		int y = 72;
		
		System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
		System.out.println(x / y);
    }
}