//관계연산자 -> 질문형이라는것 기억! 값이 true/false로 나옴~

public class RelOper {
	public static void main(String args[]) {
		System.out.println(55 == 55); //같냐?
		System.out.println(55 != 55); //다르냐?
		System.out.println(55 > 55); 
		System.out.println(55 < 55);
		System.out.println(55 >= 55); // =>(X)
		System.out.println(55 <= 55); // =<(X) 
	}
}