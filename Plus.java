public class Plus {
    public static void main(String[] args) {
        // method (메소드) call (호출) 
        System.out.println(10 + "10"); // 문자열에서 +는 결합! 달라붙는다
        System.out.println("20" + 20);
        System.out.println(); // 빈줄 출력
        System.out.println(5.123);
        System.out.println(0.1 + 0.1);

        System.out.print(1); // println은 줄바꿈O, 그냥 print는 줄바꿈X
        System.out.print(2);
        System.out.print(3);
    }
}